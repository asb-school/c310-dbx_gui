    public void setAdminState(boolean state)
    {
        if (state)
        {
            npcBtn.setEnabled(true);
            regionButton.setEnabled(true);
            npcTypeBtn.setEnabled(true);
        }
        else
        {
            npcBtn.setEnabled(false);
            regionButton.setEnabled(false);
            npcTypeBtn.setEnabled(false);
        }
    }
    
